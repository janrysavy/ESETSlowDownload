void Download(HINTERNET hInet, const char* url)
{
    std::cout << url << "\n";
    HINTERNET hInetUrl = InternetOpenUrl(hInet, url, NULL, 0, INTERNET_FLAG_RELOAD | INTERNET_FLAG_NO_CACHE_WRITE, NULL);
    if (!hInetUrl)
    {
        std::cout << "InternetOpenUrl failed, err:" << GetLastError() << "\n";
        exit(1);
    }
    const DWORD BUFF_SIZE = 10 * 1024;
    char buff[BUFF_SIZE];
    DWORD dwBytesRead;
    do
    {
        if (!InternetReadFile(hInetUrl, buff, BUFF_SIZE, &dwBytesRead))
        {
            std::cout << "InternetReadFile failed, err:" << GetLastError() << "\n";
            exit(1);
        }
    } while (dwBytesRead == BUFF_SIZE);
    InternetCloseHandle(hInetUrl);
}

